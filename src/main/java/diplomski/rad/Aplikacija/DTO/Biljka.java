package diplomski.rad.Aplikacija.DTO;

public class Biljka {

    private String ime;
    private String mesecSadnje;
    private String mesecBerbe;
    private String mestoSadnje;
    private String dubinaSadnje;
    private String svetlo;
    private String voda;
    private String napomena;

    public Biljka() {

    }

    public String getIme() {
        return ime;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }

    public String getMesecSadnje() {
        return mesecSadnje;
    }

    public void setMesecSadnje(String mesecSadnje) {
        this.mesecSadnje = mesecSadnje;
    }

    public String getMesecBerbe() {
        return mesecBerbe;
    }

    public void setMesecBerbe(String mesecBerbe) {
        this.mesecBerbe = mesecBerbe;
    }

    public String getMestoSadnje() {
        return mestoSadnje;
    }

    public void setMestoSadnje(String mestoSadnje) {
        this.mestoSadnje = mestoSadnje;
    }

    public String getDubinaSadnje() {
        return dubinaSadnje;
    }

    public void setDubinaSadnje(String dubinaSadnje) {
        this.dubinaSadnje = dubinaSadnje;
    }

    public String getSvetlo() {
        return svetlo;
    }

    public void setSvetlo(String svetlo) {
        this.svetlo = svetlo;
    }

    public String getVoda() {
        return voda;
    }

    public void setVoda(String voda) {
        this.voda = voda;
    }

    public String getNapomena() {
        return napomena;
    }

    public void setNapomena(String napomena) {
        this.napomena = napomena;
    }
}
